package javaPackage;

public class Inheritance3 {

	int z; // declaration

	public void add(int x, int y) {

		z = x + y; // Establish Relation
		System.out.println("Sum is :- " + z);
	}

	public void substract(int x, int y) {

		z = x - y; // Establish Relation
		System.out.println("Diff is :- " + z);
	}
}
