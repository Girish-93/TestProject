package javaPackage;

public class Variable9 {

	public static void main(String[] args) {

		int a; // declaration of local variable
		a = 10; // Assigning the value to a variable
		System.out.println(a); // 10

		char b = 'h'; // Defining a variable
		System.out.println(b); // Calling b variable

		String c = "a";
		System.out.println(c); // a

		String str = "Hello Selenium";
		System.out.println(str);// Hello Selenium

	}

}
